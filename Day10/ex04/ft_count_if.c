int		ft_count_if(char **tab, int (*f)(char *))
{
	int		address;
	int		nb;

	nb = 0;
	address = 0;
	while (tab[address] != 0)
	{
		if (f(tab[address]) == 1)
			nb++;
		address++;
	}
	return (nb);
}
